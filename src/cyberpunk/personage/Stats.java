package cyberpunk.personage;

public class Stats {
  float intellect;
  float reflex;
  float cool;
  float attractive;
  float luck;
  float move;
  float body;
  float empathy;

  public Stats(
    float intellect,
    float reflex,
    float cool,
    float attractive,
    float luck,
    float move,
    float body,
    float empathy
  ) {
    this.intellect = intellect;
    this.reflex = reflex;
    this.cool = cool;
    this.attractive = attractive;
    this.luck = luck;
    this.move = move;
    this.body = body;
    this.empathy = empathy;
  }
}
